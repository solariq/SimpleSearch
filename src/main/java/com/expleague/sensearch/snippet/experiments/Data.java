package com.expleague.sensearch.snippet.experiments;

public class Data {
  private String title;
  private String query;
  private String short_answer;
  private String long_answer;

  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getQuery() {
    return query;
  }

  public void setQuery(String query) {
    this.query = query;
  }

  public String getShort_answer() {
    return short_answer;
  }

  public void setShort_answer(String short_answer) {
    this.short_answer = short_answer;
  }

  public String getLong_answer() {
    return long_answer;
  }

  public void setLong_answer(String long_answer) {
    this.long_answer = long_answer;
  }
}
